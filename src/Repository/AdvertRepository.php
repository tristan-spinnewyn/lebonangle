    public function findByStatus($value){
        try {
            return $this->createQueryBuilder('a')
                ->select('COUNT(a.id) as nb_advert')
                ->andWhere('a.state = :val')
                ->setParameter('val', $value)
                ->getQuery()
                ->getScalarResult();
        }
